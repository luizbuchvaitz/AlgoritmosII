    private void botaoPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoPesquisarActionPerformed
        DefaultTableModel modeloTabela = (DefaultTableModel)tblClientes.getModel();
        String strNome = txtCliPesquisar.getText();
        
        ArrayList<Cliente> clientesFiltro = clienteDAO.pesquisar_cliente(strNome);
        
        for (int i=0; i<clientesFiltro.size(); i++) {
            Cliente cliente = clientesFiltro.get(i);
            modeloTabela.addRow(new Object[]{cliente.getNome(), cliente.getEndereco(), cliente.getFone(), cliente.getEmail()});
        }
    }//GEN-LAST:event_botaoPesquisarActionPerformed
